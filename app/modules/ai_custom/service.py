async def _stream_text_response(
    *,
    model: str,
    system_prompt: str,
    user_prompt: str,
    temperature: float = 0.3,
):
    """
    helper กลางสำหรับ stream ข้อความธรรมดา
    คืน event รูปแบบ:
    - {"type":"chunk","text":"..."}
    - {"type":"done","content":"...","usage":...,"cost":...}
    """
    final_content = ""

    async for item in call_openai_chat_stream_full(
        model=model,
        system_prompt=system_prompt,
        user_prompt=user_prompt,
        temperature=temperature,
    ):
        if item.get("type") == "chunk":
            text = item.get("text", "")
            if text:
                final_content += text
                yield {
                    "type": "chunk",
                    "text": text,
                }

        elif item.get("type") == "done":
            content = (item.get("content") or final_content).strip()
            yield {
                "type": "done",
                "content": content,
                "usage": item.get("usage"),
                "cost": item.get("cost"),
            }
            return
        
async def detect_intent(user_message: str, course_data) -> dict:
    system_prompt = f"""
    คุณคือ AI ที่ทำหน้าที่จำแนก intent ของข้อความผู้ใช้ สำหรับระบบ Self Learning

    ให้วิเคราะห์ข้อความผู้ใช้ แล้วตอบกลับเป็น JSON เท่านั้น

    intent ให้เลือกเพียง 1 ค่า จากรายการนี้:
    - greeting = ข้อความทักทาย
    - general = คุยทั่วไป ยังไม่ชัดว่าเกี่ยวกับการเรียนรู้เรื่องอะไร
    - ask_concept = ผู้ใช้อยากรู้ความหมาย อยากเข้าใจเรื่องใดเรื่องหนึ่ง
    - ask_summary = ผู้ใช้ขอให้สรุป / ย่อ / อธิบายสั้น
    - ask_application = ผู้ใช้ถามการนำไปใช้จริง หรือถามว่าควรทำอย่างไร ทำอะไรได้บ้าง
    - ask_recommend_course = ผู้ใช้ถามว่าคุณช่วยอะไรได้บ้าง มีความรู้เรื่องอะไร ควรเรียนอะไร หรือคอร์สไหนเหมาะ
    - ask_followup = ผู้ใช้ถามต่อจากบริบทเดิม เช่น ขยายเพิ่ม ยกตัวอย่างเพิ่ม
    - out_of_scope = เป็นเรื่องนอกขอบเขต self learning ชัดเจน
    - unknown = ตีความไม่ได้จริง ๆ หรือข้อความไม่มีความหมายพอ

    topic ให้เลือกเพียง 1 ค่า:
    - ถ้ามีหัวข้อที่ตรงหรือใกล้เคียงจากรายการนี้ ให้เลือก 1 ค่า
    - ถ้ายังไม่ชัด ให้ตอบ unknown

    รายการหัวข้อที่มี:
    {course_data}

    กฎสำคัญ:
    - อย่าใช้ unknown ง่ายเกินไป
    - ถ้าข้อความเป็นการคุยทั่วไป ให้เป็น general
    - ถ้าผู้ใช้ถามเกี่ยวกับ "AI" เช่น "คุณทำอะไรได้บ้าง", "ช่วยอะไรได้บ้าง", "มีความรู้อะไร" ให้เป็น ask_recommend_course
    - ถ้าผู้ใช้ถามเกี่ยวกับหัวข้อ เช่น "การสอนงานทำอะไรได้บ้าง", "Coaching มีประโยชน์อะไร", "Feedback ใช้ทำอะไร" ให้เป็น ask_application
    - ถ้าข้อความไม่เกี่ยวกับการเรียนรู้และเป็นเรื่องอื่นชัดเจน เช่น สัตว์เลี้ยง อาหาร ดารา ข่าวทั่วไป ให้เป็น out_of_scope
    - ถ้าหา topic ไม่เจอ ก็ยังเลือก intent ที่ใกล้ที่สุดได้
    - topic ไม่จำเป็นต้องมีเสมอ
    - ใช้ unknown เฉพาะเมื่อข้อความสั้นมาก แปลไม่ออก หรือไม่มีเจตนาชัดจริง ๆ
    - ถ้าผู้ใช้พูดสั้น ๆ เช่น "แล้วมีอะไรอีก", "มีอีกไหม", "แล้วต่อ", "เพิ่มอีกหน่อย" และมีบริบทก่อนหน้า ให้เป็น ask_followup

    ตัวอย่าง:
    - "สวัสดี" => greeting
    - "คุณทำอะไรได้บ้าง" => ask_recommend_course
    - "มีความรู้เรื่องอะไรบ้าง" => ask_recommend_course
    - "วันนี้เหนื่อยมาก" => general
    - "ฉันอยากรู้เรื่องการทำงาน" => ask_concept
    - "feedback คืออะไร" => ask_concept
    - "ช่วยสรุปเรื่องการสื่อสาร" => ask_summary
    - "ถ้าลูกทีมไม่เปิดใจควรทำยังไง" => ask_application
    - "ช่วยยกตัวอย่างเพิ่ม" => ask_followup
    - "แมวของฉันสีส้ม" => out_of_scope
    - ถ้าผู้ใช้ถามว่า "มันทำอะไรได้", "มีประโยชน์อะไร", "ช่วยอะไรได้", "เอาไปใช้ยังไง" ให้เป็น intent = ask_application
    - "แล้วมีอะไรอีก" → ask_followup
    - "มีอีกไหม" → ask_followup
    - "ขอเพิ่มอีกหน่อย" → ask_followup
    - "..." => unknown

    ตอบเป็น JSON เท่านั้น:
    {{
    "intent": "greeting|general|ask_concept|ask_summary|ask_application|ask_recommend_course|ask_followup|out_of_scope|unknown",
    "topic": "ชื่อ topic จากรายการ หรือ unknown"
    }}
    """.strip()

    result = await call_openai_chat_full(
        model="gpt-4.1-nano",
        system_prompt=system_prompt,
        user_prompt=user_message,
        temperature=0.1,
    )

    text = (result.get("content") or "").strip()
    text = clean_json(text)

    intent = "unknown"
    topic = "unknown"

    try:
        data = json.loads(text)
        intent = str(data.get("intent", "unknown")).strip().lower()
        topic = str(data.get("topic", "unknown")).strip()
    except Exception:
        pass

    allowed_intents = [
    "greeting",
    "general",
    "ask_concept",
    "ask_summary",
    "ask_application",
    "ask_recommend_course",
    "ask_followup",
    "out_of_scope",
    "unknown",
]

    if intent not in allowed_intents:
        intent = "unknown"

    if not topic:
        topic = "unknown"

    return {
        "intent": intent,
        "topic": topic,
    }

